    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request6 : pdcNurseOrganization.getWorkQueue().getWorkRequestList()) {
            
                Object[] row = new Object[5];
                row[0] = request6;

                row[1] = request6.getPatient();
                row[2] = request6.getMessage();
                row[3] = request6.getReceiver();
                row[4] = request6.getStatus();
                model.addRow(row);
            

        }
    }
