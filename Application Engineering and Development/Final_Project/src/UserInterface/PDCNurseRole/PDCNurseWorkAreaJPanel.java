    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            return;
        }
        WorkRequest request6 = (WorkRequest) workRequestJTable.getValueAt(selectedRow, 0);
        if (request6.getStatus().equalsIgnoreCase("Sent To Doctor")) {
            JOptionPane.showMessageDialog(null, "Request already processed.");
            return;
        } else {
            request6.setReceiver(userAccount);
            request6.setStatus("Patient is being attended by Nurse");
            WorkRequest lastRequest = null;
            for (WorkRequest request5: request6.getPatient().getUserAccount().getWorkQueue().getWorkRequestList()){
                if (request5 instanceof RequestPatientPDCReceptionist){
                    lastRequest = request5;
                }
            }
            if(lastRequest!= null){
                lastRequest.setStatus("Patient is being attended by Nurse");
            }
            populateTable();
            JOptionPane.showMessageDialog(null, "Patient is being attended by nurse");
        }
        
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            return;
        }

        RequestPDCReceptionistPDCNurse request6 = (RequestPDCReceptionistPDCNurse) workRequestJTable.getValueAt(selectedRow, 0);
        if (request6.getStatus().equalsIgnoreCase("Patient is being attended by Nurse") || request6.getStatus().equalsIgnoreCase("Being Monitored by Nurse")) {
            request6.setStatus("Being Monitored by Nurse");
            WorkRequest lastRequest = null;
            for (WorkRequest request5: request6.getPatient().getUserAccount().getWorkQueue().getWorkRequestList()){
                if (request5 instanceof RequestPatientPDCReceptionist){
                    lastRequest = request5;
                }
            }
            if(lastRequest!= null){
                lastRequest.setStatus("Being Monitored by Nurse");
            }
            PDCNurseProcessJPanel pdcNurseProcessJPanel = new PDCNurseProcessJPanel(userProcessContainer, request6, userAccount, enterprise);
            userProcessContainer.add("pdcNurseProcessjPanel", pdcNurseProcessJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else if (request6.getStatus().equalsIgnoreCase("Sent To Doctor")) {
            JOptionPane.showMessageDialog(null, "Already Sent To Doctor");
        } else {
            JOptionPane.showMessageDialog(null, "Please Assign yourself to the Patient First");
        }
    }//GEN-LAST:event_processJButtonActionPerformed


